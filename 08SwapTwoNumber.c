// Write a program in c, which swaps two variables.
#include <stdio.h>

int main(){
	
	// Declaring Variable
	int a = 10;
	int b = 20;
	int c;
	// Calcuatation to Swap
	c = a;
	a = b;
	b = c;
	
	
	
	
	// Print Value
	printf("The value of a is %d.\n",a);
	printf("The value of b is %d.\n",b);
	
	
	
	return 0;
}

